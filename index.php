<span style="font-family:Verdana, Arial, Helvetica, sans-serif; font-size:12px; font-weight:bold; color:#0000FF">Instruction to use the Tibetan Keyboard:</span>
 <ol><li>Click on "UniTibKbdMgr.exe" shortcut on the Desktop screen</li> 
<li>Click "ScrollLock" in your keyboard to toggle between typing in "English" and "Tibetan"</li> 
<li>When toggled to "Tibetan" just type in whatever tibetan words you wish, into the fields on the website.</li>
</ol>
