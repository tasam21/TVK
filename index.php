   <span style="font-family:Verdana, Arial, Helvetica, sans-serif; font-size:12px; font-weight:bold; color:#0000FF">Instruction to install Tibetan Unicode Keyboard on Window Computer:</span>
 <ol>
 <li>Unzip the downloaded zip file</li>
 <li>Folder "TCRCunicode" contain some files</li> 
<li>Copy/Install the font file "TibetanUnicode.ttf" into "Settings/Control Panels/Fonts" folder. This will install the Tibetan Font locally.</li> 
<li>In the folder "TCRCunicode", Right click "UniTibKbdMgr.exe" to create a desktop shortcut</li> 
</ol>
